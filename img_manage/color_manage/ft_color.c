int		ft_color(double shade, int r, int g, int b)
{
	if (r < 0 || r > 255 || b < 0 || b > 255 || g < 0 || g > 255)
		return (-2147483648);
	r -= r * shade;
	g -= g * shade;
	b -= b * shade;
	return (r << 16 | g << 8 | b);
}